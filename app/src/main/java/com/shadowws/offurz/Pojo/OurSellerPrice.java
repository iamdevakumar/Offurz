public class OurSellerPrice {
    String type;
    String amount;
    String cupons;
    String deals_promos;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getCupons() {
        return cupons;
    }

    public void setCupons(String cupons) {
        this.cupons = cupons;
    }

    public String getDeals_promos() {
        return deals_promos;
    }

    public void setDeals_promos(String deals_promos) {
        this.deals_promos = deals_promos;
    }
}
